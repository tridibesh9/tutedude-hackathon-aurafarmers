const Navigation = ({ userRole }) => {
  // Define all possible navigation items
  const navItems = [
    {
      id: "dashboard",
      icon: HomeIcon,
      label: "Dashboard",
      path: "/dashboard",
      roles: ["buyer", "seller"],
    },
    // { id: 'products', icon: ProductIcon, label: 'Products', path: '/products', roles: ['buyer', 'seller'] },
    {
      id: "cart",
      icon: CartIcon,
      label: "Cart",
      path: "/cart",
      roles: ["buyer"],
    },
    {
      id: "group-orders",
      icon: GroupIcon,
      label: "Group Orders",
      path: "/group-orders",
      roles: ["buyer"],
    },
    {
      id: "bargain",
      icon: BargainIcon,
      label: "Bargain",
      path: "/bargain",
      roles: ["buyer", "seller"],
    },
    {
      id: "inventory",
      icon: CartIcon,
      label: "Inventory",
      path: "/inventory",
      roles: ["seller"],
    },
    {
      id: "tracking",
      icon: TrackIcon,
      label: "My Orders",
      path: "/tracking",
      roles: ["buyer"],
    },
    {
      id: "tracking",
      icon: TrackIcon,
      label: "Tracking",
      path: "/orders",
      roles: ["seller"],
    },
    {
      id: "surplus",
      icon: ExchangeIcon,
      label: "Surplus",
      path: "/surplus",
      roles: ["buyer", "seller"],
    },
    // { id: 'khata', icon: LedgerIcon, label: 'Ledger', path: '/khata', roles: ['buyer', 'seller'] },
  ];

  // Filter items based on the current user's role
  const accessibleItems = navItems.filter((item) =>
    item.roles.includes(userRole)
  );

  const [isVisible, setIsVisible] = React.useState(true);
  const [lastScrollY, setLastScrollY] = React.useState(0);

  React.useEffect(() => {
    const handleScroll = () => {
      const currentScrollY = window.scrollY;
      setIsVisible(currentScrollY <= lastScrollY || currentScrollY < 10);
      setLastScrollY(currentScrollY);
    };

    window.addEventListener("scroll", handleScroll, { passive: true });
    return () => window.removeEventListener("scroll", handleScroll);
  }, [lastScrollY]);

  return (
    <nav className={`bottom-nav ${!isVisible ? "hidden" : ""}`}>
      <div className="bottom-nav-content">
        {accessibleItems.map((item) => {
          const Icon = item.icon;
          return (
            <NavLink
              key={item.id}
              to={item.path}
              className={({ isActive }) =>
                `nav-item ${isActive ? "active" : ""}`
              }
            >
              <Icon />
              <span>{item.label}</span>
            </NavLink>
          );
        })}
      </div>
    </nav>
  );
};

export default Navigation;
